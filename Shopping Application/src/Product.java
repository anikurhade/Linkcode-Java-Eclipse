
public class Product {

	String ProdName;
    int Prodid;
    int Prodquant;
    float ProdPrice;
    float ProdTotal;
	public float getProdTotal() {
		return ProdTotal;
	}
	public void setProdTotal(float prodTotal) {
		ProdTotal = prodTotal;
	}
	public String getProdName() {
		return ProdName;
	}
	public void setProdName(String prodName) {
		ProdName = prodName;
	}
	public int getProdid() {
		return Prodid;
	}
	public void setProdid(int prodid) {
		Prodid = prodid;
	}
	public int getProdquant() {
		return Prodquant;
	}
	public void setProdquant(int prodquant) {
		Prodquant = prodquant;
	}
	public float getProdPrice() {
		return ProdPrice;
	}
	public void setProdPrice(float prodPrice) {
		ProdPrice = prodPrice;
	}
    
}
